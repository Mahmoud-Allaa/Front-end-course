/*************************** Converts ***************************/ 

// convert Number to String 
let x_num = 5;
let y_num = x_num.toString();
console.log(x_num, typeof x_num);
console.log(y_num, typeof y_num);

// convert String to Number
let x_str = '10'
let y_str = Number(x_str);
console.log(x_str, typeof x_str);
console.log(y_str, typeof y_str);
/*************************** Loops ***************************/ 

/*
    * For Loop
    * While Loop
*/

// For Loop
for ("expression 1";" expression 2"; "expression 3") {
    // code block to be executed
}

// example
for (let i = 0; i < 5; i++) {
    text += "The number is " + i + "<br>";
}

///////////////////////////////////////////////////////

// While Loop
while (condition) {
    // code block to be executed
}

// example
i = 0;
while (i < 10) {
    text += "The number is " + i;
    i++;
}

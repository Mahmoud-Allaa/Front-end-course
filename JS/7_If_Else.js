/*************************** If & Else & Else If ***************************/ 

if (condition) {
    //  block of code to be executed if the condition is true
}
else if (condition) {
    //  block of code to be executed if the condition is true
}
else {
    //  block of code to be executed if the condition is true
}

// example
let age = 20;
if (age > 18) {
    console.log("Man"); 
}
else if (age == 18) {
    console.log("Young"); 
}
else {
    console.log("Child");
}

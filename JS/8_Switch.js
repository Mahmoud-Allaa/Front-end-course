/*************************** Switch ***************************/ 

switch(expression) {
    case x:
      // code block
      break;
    case y:
      // code block
      break;
    default:
      // code block
}

// example
let day = 0;
switch (day) {
    case 0:
      console.log("Sunday")
      break;
    case 1:
      console.log("Monday")
      break;
    case 2:
       console.log("Tuesday")
      break;
    case 3:
      console.log("Wednesda")
      break;
    case 4:
      console.log("Thursday")
      break;
    case 5:
      console.log("Friday")
      break;
    case 6:
      console.log("Saturday")
}
